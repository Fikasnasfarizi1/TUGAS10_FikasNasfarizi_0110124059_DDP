def lpersegi(sisi):
    hasil = sisi * sisi
    print(f"hasil perhitungan luas persegi adalah {hasil}")
    
def lpersegipanjang(panjang, lebar):
    hasil = panjang * lebar
    print(f"hasil perhitungan luas persegi panjang adalah {hasil}")

def llingkaran(jari):
    hasil = 22/7 * jari*jari
    print(f"hasil perhitungan luas lingkaran adalah {hasil}")

def ljajargenjang(alas,tinggi):
    hasil = alas * tinggi
    print(f"hasil perhitungan luas jajargenjang adalah {hasil}")

def lsegitiga(alas,tinggi):
    hasil = 1/2 * alas * tinggi
    print(f"hasil perhitungan luas segitiga adalah {hasil}")

