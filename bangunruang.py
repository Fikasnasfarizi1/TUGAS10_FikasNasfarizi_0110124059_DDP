def lkubus(sisi):
    hasil = 6*sisi**2
    print(f"hasil perhitungan luas kubus adalah {hasil}")

def lbalok(panjang, lebar, tinggi):
    hasil = panjang*lebar*tinggi*2
    print(f"hasil perhitungan luas balok adalah {hasil}")

def ltabung(luas_alas, luas_sisi):
    hasil = luas_alas + luas_sisi * 2
    print(f"hasil perhitungan luas tabung adalah {hasil}")

def llimas(sisi):
    hasil = sisi ** 2 + sisi ** 2
    print(f"hasil perhitungan luas limas adalah {hasil}")

def lprisma(sisi, tinggi):
    hasil = sisi ** 2 + sisi ** 2
    print(f"hasil perhitungan luas prisma adalah {hasil}")





