
def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print(f"hasil penjumlahan dari {bil1} + {bil2} adalah {hasil}")

def kurang(bil1, bil2):
    hasil = bil1 - bil2
    print(f"hasil pengurangan dari {bil1} - {bil2} adalah {hasil}")

def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print(f"hasil pembagian dari {bil1} / {bil2} adalah {hasil}")

def kali(bil1, bil2):
    hasil = bil1 * bil2
    print(f"hasil perkalian dari {bil1} * {bil2} adalah {hasil}")


